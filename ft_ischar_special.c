#include "libft.h"

int	ft_ischar_special(char c)
{
	if ((c >= 33 && c <= 47) || (c >= 58 && c <= 64) \
		|| (c >= 91 && c <= 96) || (c >= 123 && c <= 126))
		return (c);
	return (0);
}