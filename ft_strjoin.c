#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		len_s1;
	int		i;
	size_t	strlen;

	i = 0;
	len_s1 = ft_strlen(s1);
	strlen = len_s1 + ft_strlen(s2);
	str = malloc(sizeof(*s1) * (strlen + 1));
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	i = 0;
	while (s2[i])
	{
		str[len_s1] = s2[i];
		i++;
		len_s1++;
	}
	str[len_s1] = '\0';
	return (str);
}
